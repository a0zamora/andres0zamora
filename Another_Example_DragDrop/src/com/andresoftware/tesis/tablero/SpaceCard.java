package com.andresoftware.tesis.tablero;


public class SpaceCard {
	private int coordX;
	private int coordY;
	public void setCoord(int x, int y){
		coordX=x;
		coordY=y;
	}
	//----------------------------------------------------------------------------------
	public int getCoordX(){
		return coordX;
	}
	//----------------------------------------------------------------------------------
	public int getCoordY() {
		return coordY;
	}
	//----------------------------------------------------------------------------------
}
